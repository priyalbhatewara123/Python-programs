#to join list elements
print('* '.join(['cat','dog','bat']))

#to leave blank spaces on left/right/center side
print("hello".rjust(20))
print('hello'.rjust(20,'*'))
print("hello".ljust(20))
print("hello".ljust(20,"*"))
print("hello".center(20,'*'))

#to remove spaces
print("      hello      ".strip())
print("      hello      ".rstrip())
print("      hello      ".lstrip())
